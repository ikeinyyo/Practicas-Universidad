	/**
	 * It calculates the magnitude and orientation applying a gaussian filter
	 * @param src
	 * @return
	 * @throws JIPException 
	 */
	public static JIPBmpFloat filterCalculation (JIPBmpByte src) throws JIPException {
	   float[] pixelsMag, pixelsOrien;
	   float[] pixelsTempx, pixelsTempy;
	   float[] mask;
	   JIPBmpFloat res;
	   JIPBmpFloat dfx, dfy, fx, fy, aux, tempx, tempy;
	   ConvolveImage fci;
	   
	   // Converts to float for better calculus, we do not convert to float (0..1) but keep the (0..255) values
	   JIPBmpFloat srcFloat = (JIPBmpFloat)JIPImage.newImage(src.getWidth(), src.getHeight(), ImageType.FLOAT);
	   srcFloat.setAllPixels(src.getAllPixels());

	   res = (JIPBmpFloat)JIPImage.newImage(2, src.getWidth(), src.getHeight(), ImageType.FLOAT);
	   fci = new ConvolveImage();
	   mask = new float[9];

	   fx = (JIPBmpFloat)JIPImage.newImage(9, 1, ImageType.FLOAT);
	   fy = (JIPBmpFloat)JIPImage.newImage(1, 9, ImageType.FLOAT);
	   // 1-d gaussian kernels
	   mask[0] = 0.0003677f;
	   mask[1] = 0.0077312f;
	   mask[2] = 0.066709f;
	   mask[3] = 0.2408f;
	   mask[4] = 0.36876f;
	   mask[5] = 0.2408f;
	   mask[6] = 0.066709f;
	   mask[7] = 0.0077312f;
	   mask[8] = 0.0003677f;
	   fx.setAllPixelsFloat(mask);
	   fy.setAllPixelsFloat(mask);
	   
	   dfx = (JIPBmpFloat)JIPImage.newImage(9, 1, ImageType.FLOAT);
	   dfy = (JIPBmpFloat)JIPImage.newImage(1, 9, ImageType.FLOAT);
	   // 1-d gaussian kernels (derivatives)
	   mask[0] =  0.0012862f;
	   mask[1] =  0.020078f;
	   mask[2] =  0.1145f;
	   mask[3] =  0.20546f;
	   mask[4] =  0.0f;
	   mask[5] = -0.20546f;
	   mask[6] = -0.1145f;
	   mask[7] = -0.020078f;
	   mask[8] = -0.0012862f;
	   dfx.setAllPixelsFloat(mask);
	   dfy.setAllPixelsFloat(mask);

	   fci.setParamValue("image", fx);
	   aux=(JIPBmpFloat) fci.processImg(srcFloat);
	   fci.setParamValue("image", dfx);
	   tempx=(JIPBmpFloat) fci.processImg(aux);
	   fci.setParamValue("image", fy);
	   aux=(JIPBmpFloat) fci.processImg(srcFloat);
	   fci.setParamValue("image", dfy);
	   tempy=(JIPBmpFloat) fci.processImg(aux);
	   
	   pixelsTempx=tempx.getAllPixelsFloat();
	   pixelsTempy=tempy.getAllPixelsFloat();
	   pixelsMag=new float[pixelsTempx.length];
	   pixelsOrien=new float[pixelsTempx.length];

	   // Calculates the magnitude and orientation of the gradient
	   for (int i=0; i<pixelsTempx.length; i++) {
		   pixelsMag[i]=(int)Math.sqrt(pixelsTempx[i]*pixelsTempx[i]+pixelsTempy[i]*pixelsTempy[i]);
		   pixelsOrien[i]=(float)Math.atan2(-pixelsTempy[i], pixelsTempx[i]);
	   }
	   res.setAllPixelsFloat(0,pixelsOrien);
	   res.setAllPixelsFloat(1,pixelsMag);
	   
	   return res;
	}
	
